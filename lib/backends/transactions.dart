int addition(int oneNumber, int twoNumber) {
  return oneNumber + twoNumber;
}

int subtraction(int oneNumber, int twoNumber) {
  return oneNumber - twoNumber;
}

int division(int oneNumber, int twoNumber) {
  return oneNumber ~/ twoNumber;
}

int multiplication(int oneNumber, int twoNumber) {
  return oneNumber * twoNumber;
}
